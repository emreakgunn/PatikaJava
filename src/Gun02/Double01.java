package Gun02;

public class Double01 {
    public static void main(String[] args) {
        float vFloat = 3.14f;
        System.out.println(vFloat);    //float tipinin sonuna f koymalısın değerin yanına.

        double vDouble = 3.25;
        System.out.print(vDouble);      //double genelde daha çok kullanılır.
    }
}
