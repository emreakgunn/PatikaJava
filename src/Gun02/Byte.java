package Gun02;

public class Byte {
    public static void main(String[] args) {

        byte vByte = 127;               // byte veri tipi
        System.out.println(vByte);

        short vShort = 1000;        //short veri tipi kullanımı
        System.out.println(vShort);

        long vLong = 2000000000;
        System.out.println(vLong);
    }
}
